"""Corpus seed tests for 0010-i64-arith-no-trap (P17).

Verifies that the seed task files are internally consistent and that the
BTOR2 reasoning interpreter confirms the expected verdict: reach_trap is
unreachable — i64 arithmetic (extend, add, wrap) never causes a trap.

Module structure:
  func 0 (main, i32 → i32):
    local.get 0; i64.extend_i32_u; i64.const 1; i64.add; i32.wrap_i64; end
"""

from __future__ import annotations

import hashlib
import json
from pathlib import Path

_SEED_DIR = (
    Path(__file__).resolve().parents[3]
    / "bench/wasm-btor2/corpus/seed/0010-i64-arith-no-trap"
)

_EXPECTED_SHA256 = "95c928994cf744abdcf00f3d1f62189c195c553784534668d7d92dbcf0b679aa"


def _load_wasm() -> bytes:
    return (_SEED_DIR / "module.wasm").read_bytes()


def _load_spec() -> dict:
    return json.loads((_SEED_DIR / "spec.json").read_text())


